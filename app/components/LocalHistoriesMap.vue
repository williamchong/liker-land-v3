<script setup lang="ts">
const props = defineProps<{
  activeRegion?: string | null
  selectedRegion?: string | null
}>()

const emit = defineEmits<{
  (e: 'region-hover', region: string | null): void
  (e: 'region-click', region: string): void
}>()

const svgRef = ref<SVGSVGElement | null>(null)
const mapTransform = ref('translate(0px, 0px) scale(1)')

const handleMove = (event: MouseEvent) => {
  const target = event.target as Element | null
  const region = target?.getAttribute?.('data-region')
  emit('region-hover', region || null)
}

const handleLeave = () => {
  emit('region-hover', null)
}

const handleClick = (event: MouseEvent) => {
  const target = event.target as Element | null
  const region = target?.getAttribute?.('data-region')
  if (!region) return
  emit('region-click', region)
}

const updateTransform = async (region?: string | null) => {
  if (!region) {
    mapTransform.value = 'translate(0px, 0px) scale(1)'
    return
  }

  await nextTick()
  const svg = svgRef.value
  if (!svg) {
    mapTransform.value = 'translate(0px, 0px) scale(1)'
    return
  }

  const nodes = svg.querySelectorAll<SVGGraphicsElement>(`[data-region="${region}"]`)
  if (!nodes.length) {
    mapTransform.value = 'translate(0px, 0px) scale(1)'
    return
  }

  let minX = Number.POSITIVE_INFINITY
  let minY = Number.POSITIVE_INFINITY
  let maxX = Number.NEGATIVE_INFINITY
  let maxY = Number.NEGATIVE_INFINITY

  nodes.forEach((node) => {
    const { x, y, width, height } = node.getBBox()
    minX = Math.min(minX, x)
    minY = Math.min(minY, y)
    maxX = Math.max(maxX, x + width)
    maxY = Math.max(maxY, y + height)
  })

  const width = Math.max(maxX - minX, 1)
  const height = Math.max(maxY - minY, 1)
  const padding = Math.max(width, height) * 0.22
  const paddedWidth = width + padding * 2
  const paddedHeight = height + padding * 2
  const minExtent = 260
  const finalWidth = Math.max(paddedWidth, minExtent)
  const finalHeight = Math.max(paddedHeight, minExtent)
  const centerX = (minX + maxX) / 2
  const centerY = (minY + maxY) / 2

  const scale = Math.min(3.2, 1000 / Math.max(finalWidth, finalHeight))
  const translateX = 500 - centerX * scale
  const translateY = 500 - centerY * scale

  mapTransform.value = `translate(${translateX}px, ${translateY}px) scale(${scale})`
}

watch(
  () => props.selectedRegion,
  (region) => {
    updateTransform(region)
  },
  { immediate: true, flush: 'post' },
)

onMounted(() => {
  updateTransform(props.selectedRegion)
})
</script>

